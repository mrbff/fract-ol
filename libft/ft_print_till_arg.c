#include "libft.h"

size_t	ft_print_till_arg(char *s, size_t *pret)
{
	size_t	i;

	i = 0;
	while (s[i] && s[i] != '%')
	{
		ft_putchar_fd(s[i], 1);
		i++;
		(*pret)++;
	}
	return (i);
}
