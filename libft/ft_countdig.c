#include "libft.h"

size_t	ft_countdig(long int n)
{
	size_t	dig;

	if (n == 0)
		return (1);
	dig = 0;
	if (n < 0)
		n = -n;
	while (n > 0)
	{
		n /= 10;
		dig++;
	}
	return (dig);
}
